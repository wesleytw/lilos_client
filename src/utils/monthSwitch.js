export const monthSwitch = async (a) => {
  let b
  switch(a){
    case 1: b = "Jan";
        break;
    case 2: b = "Feb";
        break;
    case 3: b = "Mar";
        break;
    case 4: b = "Apr";
        break;
    case 5: b = "May";
        break;
    case 6: b = "Jun"; 
        break;
    case 7: b = "Jul";
        break;
    case 8: b = "Aug";
        break;
    case 9: b = "Sep";
        break;
    case 10: b = "Oct";
        break;
    case 11: b = "Nov";
        break;
    case 12: b = "Dec";
        break;
    }
    return b;
}
